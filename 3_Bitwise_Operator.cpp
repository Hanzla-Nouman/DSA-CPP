#include <iostream>
using namespace std;

int main()
{
   int a = 4 , b = 8;
   cout<<(a & b)<<endl; // Bitwise & AND
   cout<<(a | b)<<endl; // Bitwise | OR
   cout<<(a | b)<<endl; // Bitwise ^ XOR
   cout<<(a >> b)<<endl; // Bitwise >> Right Shift Operator
   cout<<(a << b)<<endl; // Bitwise >> Left Shift Operator

    return 0;
}